def test_python_comments_follow_project_style() -> None:
    roots = [Path("app"), Path("alembic")]
    comments: list[str] = []
    for root in roots:
        for path in root.rglob("*.py"):
            with path.open("rb") as source:
                for token in tokenize.tokenize(source.readline):
                    if token.type != tokenize.COMMENT:
                        continue
                    value = token.string.removeprefix("#").strip()
                    if value.startswith("!"):
                        continue
                    comments.append(value)

    assert comments
    assert set(comments) <= ALLOWED_COMMENTS
